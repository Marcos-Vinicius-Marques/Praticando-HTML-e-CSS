/* Fiz as funções selecionando individualmente cada elemento da Section 4, pois não tem como usar o .addEventListener 
com o .querySelectorAll para selecionar todos os elementos de uma vez só. */

let div1 = document.querySelector('.picture1')
let pic1 = document.querySelector('.content-picture1') 

div1.addEventListener('mouseover', over1)
div1.addEventListener('mouseout', out1) 

function over1() {
    pic1.style.display = 'flex'
}
function out1() {
    pic1.style.display = 'none'
}




